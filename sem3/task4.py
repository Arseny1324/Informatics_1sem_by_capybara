def print_triangle(size, symb):
    """
    Рекурсивно выводит прямоугольный треугольник.
    
    Параметры:
        size (int): количество строк треугольника
        symb (str): символ, из которого строится треугольник
    """
    def rec(row):
        # База рекурсии: если вышли за пределы size — останавливаемся
        if row > size:
            return
        # Печатаем текущую строку: row раз повторяем символ
        print(symb * row)
        # Рекурсивный вызов для следующей строки
        rec(row + 1)
    
    # Запускаем рекурсию с первой строки
    rec(1)


# Пример использования:
if __name__ == "__main__":
    # Можно протестировать прямо здесь:
    print("Пример 1:")
    print_triangle(4, '*')
    
    print("\nПример 2:")
    print_triangle(5, '#')
    
    print("\nПример 3:")
    print_triangle(3, 'x')